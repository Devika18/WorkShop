public class FindEvenOdd {
    public static void main(String[] args) {
        System.out.println("Welcome to Even and Odd Program");
        int num = 14;
        if(num%2==0) {
            System.out.println(num +" : is a Even Number");
        }
        else
            System.out.println(num+" : is a Odd Number");
    }
}
